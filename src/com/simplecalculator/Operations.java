package com.simplecalculator;

public class Operations {
	
	public double sum(double no1 , double no2) {
		return no1 + no2;
	}
	
	public double sub(double no1 , double no2) {
		return no1 - no2;
	}
	
	public double mul(double no1 , double no2) {
		return no1 * no2;
	}
	
	public double div(double no1 , double no2) {
		return no1 / no2;
	}
	
	public double sqr(double no1 , double no2) {
		return Math.pow(no1, no2);
	}
	
	public double sqrt(double no) {
		return Math.sqrt(no);
	}
	

}
